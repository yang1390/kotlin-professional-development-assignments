package nl.freshminds

import java.time.LocalDate

/**
 * 1. Write an extension function on [LocalDate] with the name [isBetween]. This function takes 2 LocalDates as inputs
 * and returns true if the receiver object is a date between these 2 dates. Returns false otherwise.
 *
 * 2. Write an extesion function on [List<Int>] with the name [squareNumbers]. This function should square each
 * element in the list. So if the original list contains [2,3,4,5] the resulting list contains [4, 9, 16, 25].
 *
 * 3. Write an extension function on the [String] class with the name [isPalindrome]. This function returns true
 * if the given string is a palindrome (it reads the same both ways, e.g. 'racecar') and false otherwise.
 *
 * 4. Create an extension function for the [String] class called [capitalizeWords] that capitalizes the first
 * letter of each word in a given string. For example, "hello world" should be transformed into "Hello World".
 */


fun LocalDate.isBetween(startDate: LocalDate, endDate: LocalDate) : Boolean =
    this.isAfter(startDate) && this.isBefore(endDate)


fun List<Int>.squareNumbers() = this.map { myInt -> myInt * myInt }


fun String.isPalindrome() : Boolean = this == this.reversed()

fun String.capitalizeWords() : String =
    this.split(" ").joinToString(" ") { word -> word.replaceFirstChar { char -> char.uppercaseChar() } }

